package sec04._02_01;

public class IfExam {
	public static void main(String[] args) {
		int score = 93;
		
		if (score >= 90) {
			System.out.println("over 90");
			System.out.println("A");
		} 
		
		if (score < 90) {
			System.out.println("Not over 90");
			System.out.println("B");
		}
	}
}
