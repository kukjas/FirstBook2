package sec04._02_02;

public class IfElseIfElseExam {
	public static void main(String[] args) {
		
		int score = 65;
		
		if (score>=90) {
			System.out.println("1등급");
		}else if (score>=80)  {
			System.out.println("2등급");
		}else if (score>=70) {
			System.out.println("3등급");
		}else {
			System.out.println("4등급");
		}
	}
}
